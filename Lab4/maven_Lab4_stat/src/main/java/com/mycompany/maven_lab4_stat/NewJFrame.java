    private void ImportButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImportButtonActionPerformed
        try {
            JFileChooser filechooser = new JFileChooser();
            filechooser.showOpenDialog(this);
            File selectedFile = filechooser.getSelectedFile();
            if(!selectedFile.getName().equals("ДЗ4.xlsx"))
            {
                JOptionPane.showMessageDialog(null,"Wrong file to read!","Ok",JOptionPane.INFORMATION_MESSAGE); 
            }
            else
            {
                samples = reader.readFile(selectedFile.toString());
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Problems with file to read!"+ex.getMessage(),"Ok",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_ImportButtonActionPerformed

