    private void ExportButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExportButtonActionPerformed
        try {
            JFileChooser filechooser = new JFileChooser();
            filechooser.showOpenDialog(this);
            File selectedFile = filechooser.getSelectedFile();
            String extension = getFileExtension(selectedFile.toString());
            if(!extension.equals("xlsx"))
            {
                JOptionPane.showMessageDialog(null,"Wrong type of file to write!","Ok",JOptionPane.INFORMATION_MESSAGE); 
            }
            else
            {
                reader.writeToFile(selectedFile.toString(), results, cov_results);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Problems with export!" + ex.getMessage(),"Ok",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_ExportButtonActionPerformed

